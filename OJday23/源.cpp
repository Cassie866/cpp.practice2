class Gift
{
public:
	int getValue(vector<int> gifts, int n)
	{
		map<int, int>m;
		for (int i = 0; i < n; ++i)
		{
			++m[gifts[i]];
			if (m[gifts[i]] > n / 2)
				return gifts[i];
		}
		return 0;
	}
};

class Gift
{
public:
	int getValue(vector<int> gifts, int n)
	{
		sort(gifts.begin(), gifts.end()); //超过一半的数排序之后必然排在中间 
		int middle = gifts[n / 2];
		int count = 0;
		for (int i = 0; i < n; i++)
		{
			//统计排在中间的数的个数 
			if (gifts[i] == middle)
			{
				count++;
			}
		}//如果个数大于一半，则存在超过一半的数 
		if (count > n / 2)
			return middle;
		else
			return 0;
	}
};
